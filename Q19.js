let alphabet = 'a'

if(
    alphabet === 'a' || 
    alphabet === 'e' || 
    alphabet === 'i' ||
    alphabet === 'o' ||
    alphabet === 'u'  
){
    console.log(`The alphabet '${alphabet}' is a vowel`)
} else{
    console.log(`The alphabet '${alphabet}' is a consonant`)
}
