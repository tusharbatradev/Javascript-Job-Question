// Write a program in C to accept a grade and declare the equivalent description :

// Grade	Description
// E	Excellent
// V	Very Good
// G	Good
// A	Average
// F	Fail


let grade = 'A'

if(grade === 'E'){
    console.log("Excellent")
} else if(grade === 'V'){
    console.log("Very Good")
} else if(grade === 'G'){
    console.log("Good")
} else if(grade === 'A'){
    console.log("Average")
} else if(grade === 'F'){
    console.log("Fail")
}