// Write a program in C to accept a grade and declare the equivalent description :

// Grade	Description
// E	Excellent
// V	Very Good
// G	Good
// A	Average
// F	Fail

let grade = "G";

switch (grade) {
  case "A":
    console.log("Average");
    break;
  case "F":
    console.log("Fail");
    break;
  case "G":
    console.log("Good");
    break;
  case "V":
    console.log("Very Good");
    break;
  case "E":
    console.log("Excellent");
    break;
}
